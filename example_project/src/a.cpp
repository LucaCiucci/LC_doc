

/**
 * @brief This is a function that does something.
 * 
 * This function performs the sum of two numbers:
 * <tex-math>
 *  \text{somma}(a,b) = a + b
 * </tex-math>
 * 
 * @param a This is the first parameter.
 * @param b This is the second parameter.
 * @return This is the return value.
*/
int somma(int a, int b) {
    return a + b;
}